    public void handl() throws IOException{
       ImageIcon icon;
       icon = new ImageIcon("src/data/img_avatar/avatar"+ String.valueOf(fileData.readAvatar()) +".png");
       avatar.setIcon(icon);
       String line = fileData.checkUserLogin();
       if(line != null)
             avatar.setText(line);
       int dem =0,max =0;
       float tb =0,sum =0;
       LinkedList<String> allHistory = new LinkedList<>();
       fileData.readHistory(allHistory);
       for(int i=allHistory.size()-1;i>=0;i--){
            String[] s = allHistory.get(i).split("  ");
            if(s[0].equals(line)){
                dem++;
                sum = sum + Integer.parseInt(s[1]);
                if(Integer.parseInt(s[1]) >max)
                    max = Integer.parseInt(s[1]);
            }
       }
       if(dem!=0){
           tb = ((float)(Math.round((sum/dem)*100)))/100;
       }
       java.time.LocalDateTime x =java.time.LocalDateTime.now(); 
       line1.setText("Tên đăng nhập     : "+ line+" ");
       line2.setText("Số lần thi        : " + String.valueOf(dem)+" ");
       line3.setText("Thành tích trung bình : " + String.valueOf(tb)+"/25 ");
       line4.setText("Thành tích cao nhất   : " + String.valueOf(max)+"/25 ");
       line5.setText("Avatar  : avatar" +String.valueOf(fileData.readAvatar()));
       line6.setText("Đã đăng nhập lúc  :  "+ x.getHour()+":"+x.getMinute()+" - "+x.getDayOfMonth()+"/"+x.getMonth().getValue()+"/"+x.getYear()+" " );
    }
