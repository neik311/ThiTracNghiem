    private void buttonRegister2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegister2ActionPerformed
        jLabel2.setText(" ");
        jLabel1.setText(" ");
        jLabel3.setText(" ");
        String pass ="";
        String Pass1 = new String(fieldPass2.getPassword());
        String Pass2 = new String(fieldPassN.getPassword());
        String nameRegis = fieldRegister.getText();
        if(nameRegis.length()<3 || nameRegis.length()>15 ||nameRegis.indexOf(" ") !=-1 ){
            jLabel1.setText("từ 3-15 ký tự viết liền kề nhau ");
        }
        else if(Pass1.length()<4 || Pass1.length() >15 || Pass1.indexOf(" ") !=-1){
            jLabel2.setText("từ 4-15 ký tự viết liền kề nhau ");
        }
        else if(!Pass1.equals(Pass2)){
            jLabel3.setText("mật khẩu không chính xác ");
        }
        else {
            try {
                pass =fileData.checkUser(nameRegis);
                if(pass.equals("")==false){
                    jLabel1.setText("tên đăng nhập đã tồn tại ");
                }
                else{
                    try {
                        jLabel4.setText("Tạo tài khoản thành công , nguười dùng vui lòng quay về màn hình đăng nhập ");
                        fileData.addUser(nameRegis, Pass1);
                        fieldRegister.setText("");
                        fieldPass2.setText("");
                        fieldPassN.setText("");
                    } catch (IOException ex) {
                        Logger.getLogger(userRegis.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(userRegis.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_buttonRegister2ActionPerformed

